<h1>Z čoho vychádzame?</h1>
<ul class="Kompas">
	<li>Moderná psychiatrická starostlivosť vo vyspelých európskych krajinách smeruje k previazanosti psychiatrických a sociálnych služieb.</li>
	<li>V súlade s medzinárodnými trendmi sú na Slovensku vypracované právne základy pre moderný vývoj psychiatrickej starostlivosti ako napr.:
		<ul class="Kompas">
			<li>„Nová koncepcia psychiatrie – reforma psychiatrickej starostlivosti“ (Vestník MZ, 1997)</li>
			<li>Národný program duševného zdravia (Schválený vládou SR v roku 2004)</li>
			<li>Zelená kniha (2006)</li>
		</ul>
	</li>
	<li>Žiaľ, aj keď proces transformácie psychiatrickej starostlivosti je už naštartovaný, v súčasnosti viazne.</li>
	<li>Hlavnými nedostatkami v oblasti komunitných služieb pre ľudí s chronickými psychickými poruchami sú u nás naďalej: oblasť pracovnej rehabilitácie, podporovaného zamestnania a chráneného bývania.</li>
	<li>Zatiaľ tiež neexistuje koncepcia komunitnej psychiatrickej starostlivosti formou case manažmentu (prípadový manažment).</li>
</ul>