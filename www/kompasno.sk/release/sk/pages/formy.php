<h1>Formy realizácie našich cieľov</h1>
<ul class="Kompas">
	<li>Prednášky na zdravotné a sociálne témy (so zameraním na duševné zdravie a duševné ochorenia) určená pre rôzne cieľové skupiny.</li>
	<li>Prednášky a vzdelávacie aktivity v oblasti komunitnej psychiatrickej starostlivosti pre odbornú verejnosť.</li>
	<li>Výskumná a evaluačná činnosť – monitorovanie rôznych aspektov duševného zdravia obyvateľov a hodnotenie kvality služieb v oblasti duševného zdravia.</li>
	<li>Iniciovanie regionálnej, nadregionálnej, národnej a medzinárodnej spolupráce v oblasti starostlivosti o duševné zdravie.</li>
	<li>Zapájanie sa do existujúcich projektov na podporu duševného zdravia a skvalitnenie služieb pre ľudí s duševným ochorením.</li>
	<li>Rozvoj existujúcich a vytváranie chýbajúcich služieb komunitnej psychiatrickej starostlivosti.</li>
</ul>