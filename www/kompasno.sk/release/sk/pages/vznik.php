<h1>Vznik</h1>
<p>Naša nezisková organizácia bola zriadená za účelom rozvoja komplexnej
starostlivosti o ľudí s duševným ochorením formou komunitnej starostlivosti,
ktorej cieľom je väčšia miera integrácie ľudí s duševnou poruchou do
spoločnosti. Významnou súčasťou činnosti organizácie je prevencia stigmatizácie
ľudí s duševným ochorením a vzdelávanie v oblasti komunitnej starostlivosti o
duševné zdravie.</p>
<h2>Kedy?</h2>
<p>KOMPAS – Komunitná pomoc a starostlivosť, n.o., ako nezisková organizácia
poskytujúca všeobecne prospešné služby, bola oficiálne zaregistrovaná Obvodným
úradom v Trnave, odbor všeobecnej vnútornej správy dňa 19.9.2008.</p>
<h2>Kto?</h2>
<p>Zakladajúcimi členmi neziskovej organizácie sú odborníci v oblasti duševného
zdravia – 2 psychiatri a psychológ, so skúsenosťami v oblasti starostlivosti o
ľudí s duševným ochorením a ich príbuzných.</p>
<h2>Prečo?</h2>
<p>Založenie neziskovej organizácie je reakciou na potrebu venovania zvýšenej
pozornosti oblasti duševného zdravia, vyplývajúcej z reformy starostlivosti o
duševné zdravie v SR.</p>