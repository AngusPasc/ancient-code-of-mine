<h1>An integrated network of care for persons with mental illness</h1>
<p>Among the most common problems of people with mental illness is stigmatization in society and resulting social isolation. Along with it comes an emphasis on hospitalized treatment, which in turn results in a degrading of social competencies and social relationships, as well as many problems in the everyday life after returning to the community. Many times this results in the person being in an unnecessary state of disability.</p>
<p>The foundation of community mental health care is stretching out a helping hand to people with mental illness after the acute stage of illness has passed by providing tailored services in the community. These services are in the area of housing, employment, development of social skills, and overcoming social isolation.</p>
<p>The concept of community care creates a link between <span class="Bold">hospitalized</span> and <span class="Bold">non-hospital</span> care for people with mental illness in close cooperation with stakeholders from all areas.</p>
<p>Community care includes provision of the following services:
	<ul class="Kompas">
		<li>Counseling services</li>
		<li>Case management services</li>
		<li>Vocational rehabilitation, protective and supported employment services</li>
		<li>Psycho-social rehabilitation services</li>
		<li>Free-time facilities and clubs</li>
		<li>Self-support groups</li>
		<li>Housing assistance- protective and supportive homes</li>
	</ul>
</p>