<h1>Program "TETRALÓG"</h1>
<p>Od novembra 2008 realizuje KOMPAS - Komunitná pomoc a starostlivosť, n.o. program „Tetralóg“, ktorý tvorí základ jej činnosti.</p>
<p>„Tetralóg“ znamená dialóg štyroch strán:
	<ul class="Kompas">
		<li>Ľudí so skúsenosťou s duševným ochorením</li>
		<li>Ich príbuzných a priateľov</li>
		<li>Verejnosti</li>
		<li>Odborníkov v oblasti starostlivosti o duševné zdravie</li>
	</ul>
</p>
<p>Ideu tetralógu predstavil na Slovensku MUDr. P. Nawka v rámci projektu „Reintegrácia psychosociálne postihnutých do spoločnosti“ v roku 1997.</p>
<p>Cieľom programu "Tetralóg" je iniciovať a podporovať komunikáciu a spoluprácu všetkých zúčastnených strán pri vytváraní a poskytovaní komunitnej starostlivosti o duševné zdravie v Trnavskom a Bratislavskom kraji.</p>
<p>V rámci programu „Tetralóg“ máme vymedzené nasledovné ciele:
	<ul class="Kompas">
		<li>Navrhnúť také služby, ktoré budú napĺňať potreby ľudí s duševným ochorením a potreby ich rodín, priateľov a blízkych, a ďalších, ktorí sú pre nich oporou</li>
		<li>Navrhnúť účinné formy destigmatizácie vychádzajúce z poznania postojov verejnosti</li>
		<li>Navrhnúť účinné spôsoby zapojenia odborníkov do procesu transformácie starostlivosti o duševné zdravie, vzhľadom na ich možnosti a kompetencie</li>
		<li>Využívať príležitosti na poskytovanie informácií pre verejnosť čo sa týka duševného zdravia, duševných chorôb a starostlivosti o ľudí s duševným ochorením, s cieľom prekonať tabuizovanie týchto tém a stigmatizáciu ľudí s  duševným ochorením</li>
		<li>Poukázať na nové trendy v oblasti starostlivosti o ľudí s duševným ochorením (napr. nove formy komunitných a sociálnych služieb)</li>
		<li>Podporovať motiváciu a informovanosť všetkých zainteresovaných, aby sa iniciatívne a aktívne usilovali o zlepšenie situácie ľudí s duševných ochorením a ich blízkych</li>
	</ul>
</p>